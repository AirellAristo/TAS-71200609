package com.rplbo.math;

public class Ternary{

    private int angka1;
    private int angka2;
    private int angka3;

    public Ternary(int angka1, int angka2, int angka3){
        this.angka1 = angka1;
        this.angka2 = angka2;
        this.angka3 = angka3;
    }

    public int getAngka1() {
        return angka1;
    }

    public int getAngka2() {
        return angka2;
    }

    public int getAngka3() {
        return angka3;
    }}
